package belajarweb;

public class School {
	
	public String school_name;
	public int year_in;
	public int year_out;
	public String major;

	
	
	public School(String school_name, int year_in, int year_out, String major) {
		this.school_name = school_name;
		this.year_in = year_in;
		this.year_out = year_out;
		this.major = major;
	}

}
